results = {}

for kw_bytes, kw_name in keywords_map.items():
    idx = data.find(kw_bytes)
    if idx != -1:
        print(f"\n{'='*60}")
        print(f"Found '{kw_name}' at offset 0x{idx:X}")
        print(f"{'='*60}")
        
        # Look backwards for IFR opcodes
        start = max(0, idx - 512)
        end = min(len(data), idx + 512)
        region = data[start:end]
        
        # Try to find opcodes before the string
        print(f"\nBackward analysis from 0x{idx:X}:")
        for i in range(idx - 1, max(0, idx - 256), -1):
            opcode = data[i]
            if opcode in CRITICAL_OPCODES:
                length = data[i+1] | (data[i+2] << 8) if i+2 < len(data) else 0
                print(f"  0x{i:X}: {CRITICAL_OPCODES[opcode]} (len={length})")
                # Print hex dump of this opcode
                op_end = min(len(data), i + length)
                op_data = data[i:op_end]
                print(f"    Hex: {op_data.hex()[:80]}")
                break
        
        # Try to find opcodes after the string (for string refs)
        print(f"\nForward analysis from 0x{idx:X}:")
        for i in range(idx + len(kw_bytes), min(len(data), idx + len(kw_bytes) + 256)):
            opcode = data[i]
            if opcode in CRITICAL_OPCODES:
                length = data[i+1] | (data[i+2] << 8) if i+2 < len(data) else 0
                print(f"  0x{i:X}: {CRITICAL_OPCODES[opcode]} (len={length})")
                op_end = min(len(data), i + length)
                op_data = data[i:op_end]
                print(f"    Hex: {op_data.hex()[:80]}")
                break
        
        results[kw_name] = {'offset': idx}

print(f"\n\nSummary: Found {len(results)} keywords")
for name, info in results.items():
    print(f"  {name}: 0x{info['offset']:X}")

